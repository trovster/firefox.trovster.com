<h2>About</h2>

<p><a href="http://www.mozilla.org/products/firefox/">Firefox</a> is Mozilla's award-winning next generation web browser.
The web browser is based upon the Geeko rendering engine, which is what powers the browser. The browser features
a built-in pop-up stopper as well as tabbed browsers. The combination of these technologies allows safer, more efficient
and pleasant means of browsig the internet.</p>