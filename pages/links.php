<h3><span></span>Other Good Browsers</h3>
<dl id="browsers">
<dt><a href="http://www.mozilla.org/products/mozilla1.x/" title="Mozilla Internet Suite Homepage">Mozilla</a></dt>
<dd><p>A complete internet suite with browser, email client, newsgroup client, <abbr title="Internet Relay Chat">IRC</abbr>
chat client, and <abbr title="Hypertext Markup Language">HTML</abbr> editing. Based on the gecko engine which is also used
on Netscape Navigator and Firefox.</p></dd>

<dt><a href="http://www.opera.com" title="Opera Browser Homepage">Opera</a></dt>
<dd><p>An extremely powerful, highly customisable and standards complaint browser. Features a lot of tools and extras
for power users including mouse gestures and a popup stopper. It also comes with integrated mail, news and chat clients.
The software is adware, which means it display adverts on the browser unless paid for.</p></dd>

<dt><a href="http://channels.netscape.com/ns/browsers/default.jsp" title="Netscape Navigator Browser Homepage">Netscape Navigator</a></dt>
<dd><p>A well known rebranding of the original Mozilla Browser. Also based upon the gecko engine, this browser was Internet Explorers
main competitor in the '90s.</p></dd>

<dt><a href="http://kmeleon.sourceforge.net/" title="K-Melon Browser Homepage">K-Melon</a></dt>
<dd><p>An extremely fast, customisable and lightweight browser for Windows platform. It is open source software, and uses
the Gecko rendering engine.</p></dd>

<dt><a href="http://www.konqueror.org/features/browser.php" title="Konqueror Browser (*nix)">Konqueror</a></dt>
<dd><p>A lightweight, standards compliant web browser for the <abbr title="Unix Based">*nix</abbr> operating system.</p></dd>

<dt><a href="http://www.apple.com/safari/" title="Safari Browser (Mac)">Safari</a></dt>
<dd><p>This browser is the <abbr title="Macintosh">Mac</abbr> <abbr title="Operating System">OS</abbr> X default web browser.</p></dd>
</dl>


